    private void addProductActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_addProductActionPerformed
        // TODO add your handling code here:
        if (CTPhieu == null) {
            CTPhieu = new ArrayList<ChiTietBanHang>();
        }
        int i_row = tblSanPham.getSelectedRow();
        if (i_row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm để xuất hàng !");
            return;
        }

        String maLoHangCode = tblSanPham.getValueAt(i_row, 0).toString();

        LoHang selectedProduct = SearchLoHang.getInstance().searchByMaLoHangCodeItem(maLoHangCode);
        int availableQuantity = selectedProduct.getSoLuong();
        if (availableQuantity == 0) {
            JOptionPane.showMessageDialog(this, "Sản phẩm đã hết hàng !");
            return;
        }

        int enteredQuantity;
        try {
            enteredQuantity = Integer.parseInt(txtSoLuong.getText().trim());
            if (enteredQuantity <= 0) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng lớn hơn 0");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng ở dạng số nguyên!");
            return;
        }

        if (enteredQuantity > availableQuantity) {
            JOptionPane.showMessageDialog(this, "Số lượng không đủ !");
            return;
        }

        ChiTietBanHang existingProduct = findCTPhieu(selectedProduct.getMaLoHangCode());
        if (existingProduct != null) {
            if (existingProduct.getSoLuong() + enteredQuantity > availableQuantity) {
                JOptionPane.showMessageDialog(this, "Số lượng không đủ !");
                return;
            } else {
                existingProduct.setSoLuong(existingProduct.getSoLuong() + enteredQuantity);
                existingProduct.setTongTien(existingProduct.getSoLuong() * selectedProduct.getGiaBan());
            }
        } else {
            ChiTietBanHang newOrderDetail = new ChiTietBanHang(MaPhieu, selectedProduct.getMaLoHangCode(),
                    selectedProduct.getTenSanPham(), enteredQuantity, selectedProduct.getGiaBan() * enteredQuantity);
            newOrderDetail.setMaLoHang(selectedProduct.getMaLoHang());
            CTPhieu.add(newOrderDetail);
        }

        loadDataToTableNhapHang();
    }// GEN-LAST:event_addProductActionPerformed

