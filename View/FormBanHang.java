    private void loadDataToTableProduct(ArrayList<LoHang> arrProd) {
        try {
            tblModel.setRowCount(0);
            for (var i : arrProd) {
                tblModel.addRow(new Object[]{
                    i.getMaLoHangCode(), i.getTenSanPham(), i.getSoLuong(), formatter.format(i.getGiaBan()) + "đ"
                });
            }
        } catch (Exception e) {
        }
    }

    public double tinhTongTien() {
        double tt = 0;
        for (var i : CTPhieu) {
            tt += (i.getTongTien());
        }
        return tt;
    }

    public void loadDataToTableNhapHang() {
        double sum = 0;
        try {
            DefaultTableModel tblNhapHangmd = (DefaultTableModel) tblNhapHang.getModel();
            tblNhapHangmd.setRowCount(0);

            for (int i = 0; i < CTPhieu.size(); i++) {
                tblNhapHangmd.addRow(new Object[]{
                    i + 1, CTPhieu.get(i).getMaLoHangCode(), CTPhieu.get(i).getTenSanPham(),
                    CTPhieu.get(i).getSoLuong(), formatter.format(CTPhieu.get(i).getTongTien()) + "đ"
                });
                sum += CTPhieu.get(i).getTongTien();
            }
        } catch (Exception e) {
        }
        textTongTien.setText(formatter.format(sum) + "đ");
    }

