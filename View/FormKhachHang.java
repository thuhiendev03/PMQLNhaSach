    private void jTextFieldSearchKeyPressed(java.awt.event.KeyEvent evt) {// GEN-FIRST:event_jTextFieldSearchKeyPressed
        // TODO add your handling code here:
        // TODO add your handling code here:
        String luachon = (String) jComboBoxLuaChon.getSelectedItem();
        String searchContent = jTextFieldSearch.getText();
        ArrayList<KhachHang> result = new ArrayList<>();
        switch (luachon) {
            case "Tất cả" -> result = SearchKhachHang.getInstance().searchTatCa(searchContent);
            case "Họ tên" -> result = SearchKhachHang.getInstance().searchTenKH(searchContent);
            case "Email" -> result = SearchKhachHang.getInstance().searchEmail(searchContent);
            case "Số điện thoại" -> result = SearchKhachHang.getInstance().searchSdt(searchContent);

            case "Mã khách hàng" -> result = SearchKhachHang.getInstance().searchMaKH(searchContent);
        }
        loadDataToTable(result);
    }// GEN-LAST:event_jTextFieldSearchKeyPressed

