    public final void initTable() {
        tblModel = new DefaultTableModel();
        String[] headerTbl = new String[] { "Mã khách hàng", "Tên khách hàng", "Email", "Số điện thoại", "Địa chỉ" };
        tblModel.setColumnIdentifiers(headerTbl);
        tblKhachHang.setModel(tblModel);
        tblKhachHang.getColumnModel().getColumn(0).setPreferredWidth(1);
        tblKhachHang.getColumnModel().getColumn(1).setPreferredWidth(300);
        tblKhachHang.getColumnModel().getColumn(2).setPreferredWidth(150);
        tblKhachHang.getColumnModel().getColumn(3).setPreferredWidth(150);
        tblKhachHang.getColumnModel().getColumn(4).setPreferredWidth(200);
    }

    public void loadDataToTable(ArrayList<KhachHang> kh) {
        try {
            tblModel.setRowCount(0);
            for (KhachHang i : kh) {
                tblModel.addRow(new Object[] {
                        i.getMaKHCode(), i.getHoTenKH(), i.getEmail(), i.getSdt(), i.getDiaChi()
                });
            }
        } catch (Exception e) {
        }
    }

    public KhachHang getKhachHangSelect() {
        int i_row = tblKhachHang.getSelectedRow();
        KhachHang kh = KhachHangDao.getInstance().selectAll().get(i_row);
        return kh;
    }

