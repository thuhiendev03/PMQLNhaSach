    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (tblKhachHang.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn khách hàng muốn xoá");
        } else {
            int output = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá nhà cung cấp",
                    "Xác nhận xoá nhà cung cấp", JOptionPane.YES_NO_OPTION);
            if (output == JOptionPane.YES_OPTION) {
                KhachHangDao.getInstance().delete(getKhachHangSelect());
                JOptionPane.showMessageDialog(this, "Xóa thành công !");
                loadDataToTable(KhachHangDao.getInstance().selectAll());
            }
        }
    }// GEN-LAST:event_btnDeleteActionPerformed

