    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {// GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        String luachon = (String) cbxLuaChon.getSelectedItem();
        String searchContent = txtSearch.getText();
        ArrayList<SanPham> result = new ArrayList<>();
        switch (luachon) {
            case "Tất cả":
                result = SearchSanPham.getInstance().searchTatCa(searchContent);
                break;
            case "Mã sản phẩm":
                result = SearchSanPham.getInstance().searchMaSP(searchContent);
                break;
            case "Tên sản phẩm":
                result = SearchSanPham.getInstance().searchTenSP(searchContent);
                break;
            case "Danh mục":
                result = SearchSanPham.getInstance().searchDanhMuc(searchContent);
                break;
            case "Tác giả":
                result = SearchSanPham.getInstance().searchTacGia(searchContent);
                break;
            case "Nhà xuất bản":
                result = SearchSanPham.getInstance().searchNXB(searchContent);
                break;
            case "Giá bán nhập":
                result = SearchSanPham.getInstance().searchGiaBanNhap(searchContent);
                break;
            case "Nhà cung cấp":
                result = SearchSanPham.getInstance().searchNCC(searchContent);
                break;
        }
        loadDataToTable(result);
    }// GEN-LAST:event_txtSearchKeyReleased

