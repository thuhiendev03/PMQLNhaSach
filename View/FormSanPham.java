    public void loadDataToTable(ArrayList<SanPham> sp) {
        try {
            tblModel.setRowCount(0);
            for (SanPham i : sp) {
                tblModel.addRow(new Object[] {
                        i.getMaSanPhamCode(), i.getTenSanPham(), i.getDanhMuc(), i.getTacGia(), i.getNhaXuatBan(),
                        i.getGiaBanNhap(), i.getTenNhaCungCap()
                });
            }
        } catch (Exception e) {
        }
    }

    public SanPham getSanPhamSelect() {
        int i_row = tblSanPham.getSelectedRow();
        SanPham nd = SanPhamDao.getInstance().selectAll().get(i_row);
        return nd;
    }

